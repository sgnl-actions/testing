/**
 * Detect if an action is LDAP-based from its metadata.
 *
 * @param {{ name: string, description: string }} metadata
 * @returns {boolean} True if this appears to be an LDAP action
 */
export function isLDAPAction(metadata) {
  const name = metadata.name?.toLowerCase() || '';
  const description = metadata.description?.toLowerCase() || '';
  
  // Check if it's an AD action (starts with 'ad-') or mentions LDAP in description
  return name.startsWith('ad-') || description.includes('ldap');
}
